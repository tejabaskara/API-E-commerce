package org.example;

public class addresses {
    private int users;
    private String type;
    private String city;
    private String province;
    private String postcode;

    public int getUsers() {
        return users;
    }

    public String getCity() {
        return city;
    }

    public String getPostcode() {
        return postcode;
    }

    public String getProvince() {
        return province;
    }

    public String getType() {
        return type;
    }


}
